    private void jButtonCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadActionPerformed
        if (jExclui.getText() != "")
        {
            DataSource dataSource = new DataSource();

        Pontos p = new Pontos();
        daoPontos pontosDao = new daoPontos(dataSource);

            int codigo = Integer.parseInt(jExclui.getText());
            pontosDao.remover(codigo);

            // fecha a janela
            this.dispose();

            // fecha DataSource
            dataSource.closeDataSource();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione um ponto para excluir!");
        }
    }//GEN-LAST:event_jButtonCadActionPerformed

